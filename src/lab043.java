public class lab043 {
    public static void main(String[] args) {
        //ternary operators
        // ? if true and : if false
        int a1 = (10>20)? 19: 14;
        System.out.println(a1);
        String a = 16>40 ? "30" : "25";
        System.out.println(a);
        boolean a2 = 15<22 ? true : false;
        System.out.println(a2);







    }
}
